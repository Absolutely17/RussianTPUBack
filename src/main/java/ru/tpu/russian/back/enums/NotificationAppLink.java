package ru.tpu.russian.back.enums;

/**
 * Активность, которая будет открыта по нажатию на уведомление
 */
public enum NotificationAppLink {
    /**
     * Откроется активность со списком документов
     */
    DOCUMENT,

    /**
     * Откроется активность со списком уведомлений
     */
    NOTIFICATION,

    /**
     * Откроется активность с календарем
     */
    CALENDAR_EVENT
}
